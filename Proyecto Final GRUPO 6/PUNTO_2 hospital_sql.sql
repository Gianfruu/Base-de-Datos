DROP DATABASE IF EXISTS hospital;

CREATE DATABASE hospital;

USE hospital;

CREATE TABLE pacientes(
dni INTEGER NOT NULL,
pa_apellido VARCHAR(100) NOT NULL,
pa_nombre VARCHAR(100) NOT NULL,
fecha_nacimiento DATE NOT NULL,
email VARCHAR(100) NOT NULL,
pa_telefono VARCHAR(50) NOT NULL,
obra_social VARCHAR(100) NOT NULL,
nro_obra_social VARCHAR(100) NOT NULL,
domicilio VARCHAR(100) NOT NULL,
localidad VARCHAR(100) NOT NULL,
PRIMARY KEY(dni)
);

CREATE TABLE medicos(
matricula INTEGER NOT NULL,
me_apellido VARCHAR(100) NOT NULL,
me_nombre VARCHAR(100) NOT NULL,
especialidad VARCHAR(100) NOT NULL,
me_telefono VARCHAR(50) NOT NULL,
PRIMARY KEY(matricula)
);

CREATE TABLE turnos(
tr_dni INTEGER NOT NULL,
tr_matricula INTEGER NOT NULL,
tr_fecha DATE NOT NULL,
tr_horario TIME NOT NULL,
PRIMARY KEY(tr_dni, tr_fecha, tr_horario),
INDEX(tr_fecha),
FOREIGN KEY(tr_dni) REFERENCES pacientes(dni)
	ON UPDATE CASCADE ON DELETE RESTRICT,
FOREIGN KEY(tr_matricula) REFERENCES medicos(matricula)
	ON UPDATE CASCADE ON DELETE RESTRICT
);

